#korvensi celicius ke satuan lain

print("\nPEROGRAM CELICIUS & KAWANNYA\n")

celicius = float(input('masukan suhu dalam celicius:'))
print("suhu adalah",celicius,"Celicius")

#reamur
reamur = (4/5) * celicius
print("suhu dalam reamur adalah",reamur,"Reamur")

#fahrenheit
fahrenheit = ((9/5) * celicius) + 32
print("suhu dalam fahrenheit adalah",fahrenheit,"Fahreinheit")

#kelvin
kelvin = celicius + 273
print("suhu dalam kelvin adalah",kelvin,"Kelvin")