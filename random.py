# operasi aritmatika

a = 10
b = 5

#penjumlahan
hasil = a + b 
print(a,'+',b,'=',hasil)

#pengurangan
hasil = a - b 
print(a,"-",b,"=",hasil)
#perkalian
hasil = a * b 
print (a,"*",b,"=",hasil)

#pembagian
hasil = a / b 
print (a,"/",b,"=",hasil)
#eksponen/pangkat
hasil = a ** b
print (a,"**",b,"=",hasil)
#modulus 
hasil = a % b
print (a,"%",b,"=",hasil)
#floor division
hasil = a // b
print (a,"//",b,"=",hasil)

#oprasi prioritas / oprational predente

x = 3
y = 12
z = 2
hasil = x ** y * z - y + z % x / z // y
print(x,'**',y,'*',z,'-',y,'+',z,'%',x,'/',z,'//',y,'=',hasil)

